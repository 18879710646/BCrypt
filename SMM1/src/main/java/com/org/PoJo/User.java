@Data
@NoArgsConstructor
@AllArgsConstructor
@Setter
@Getter
@ToString
public class User {
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    private int id;
    private String name;
    private int money;
}
